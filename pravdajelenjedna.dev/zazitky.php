Stretol som velmi mile dievca, len o rok mladsie odo mna, ktore ma prekvapilo so svojou otvorenostou a jej pohladmy.
Po prvej vymene zakladnych informacii, som priniesol myslienku Boha. Zastala trochu viac v soku, ale vsetko sa
vyjasnilo hned ako som jej vysvetlil, ze ja si predstavujem Boha ako nieco vrodene prelinajuce sa s Prirodou.
Nazvala ma panteistom. Hned som odmietol tento pojem, kedze nemam rad, ked sa ludia nazyvaju menami. Ale ked som si
dalsie rano cital tuto stranku a material, ktory davam do kopy, zistil som, ze panteizmus mam ako jednu z definicii
v casti Slovnik. Takze je to pre mna novy zazitok, ked ma niekto neznamy prijemne prekvapil s niecim, co som uz
niekedy videl a pripomenul mi kto som.</br>
Nas rozhovor zavialo aj ku teme zenskych opatkov a moja spolocnicka mi prezradila, ze povazuje zeny za krasne, ked nosia opatky. Po
tom ako som jej vysvetlil, ze mne sa to nepaci a ze lutujem jej chodidla, prekvapene na mna pozrela a len zopakovala,
svoj nazor. A to ma privadza k usudku, ze to nie je nazor muzov, ze zeny su krasne s opatkami, alebo ze je to nazor
spolocnosti.
Je to myslienka samotnych zien, ktore ju maju nahrane ako program ci uz zo spolocenskeho programovania, alebo 
dovodu, ze to tak robia vsetci. To sa mi vsak uz nepodarilo vysvetlit a mozno sa mi uz ani nikdy nepodari, ale ak by
predsa niekedy ta zavialo na tento web. Tak dakujem za cestu vlakom. </br></br>
*****</br>
Isiel som vlakom a oproti mne sedela zena, ktora pocuvala hudbu. Po tom ako vsetci odisli a zlozila si sluchadla som 
sa opytal, ci pocuva nieco dobre. Jej odpoved bola, Nie. Zbalila sa a odisla prec, pravdepodobne do ineho vozna. 
Ked som odchadzal aj ja, nadobudol som tusenie, ze som uz takuto situaciu presne s nou zazil.A v minulosti som sa opytal, 
ci pracuje alebo studuje, na co som dostal odpoved rovnakeho stylu, Nie. Po tychto dvoch totoznych situaciach s rovnakym
bytim som si spomenul na nieco z minulosti.</br>
Jednalo sa o prve dievca, ktore sa mi pacilo na strednej, ktore som oslovil viac ako by bolo slusne a viac rokov
 ako to bolo vhodne a ktoreho odpoved
bola z neviem akeho dovodu Nie. Kazdy ma Pravo povedat Nie a ja som to nevedel kedysi respektovat. Po tom ako som sa
to naucil respektovat, teda presnejsie naucil sa respektovat sameho seba, som sa jej to snazil vysvetlit, ale marne.
</br>
Takze akykolvek pokus ist za tou dievcinou vo vlaku a vyvinut aktivitu je zbytocny a zo sucasneho status quo povazovatelny
za obtazovanie. </br>
Ako som zistil, k takymto situaciam ma pritahuje to chladne a nekompromisne Nie. Isty cas som si vnucoval, ze to
milujem a snazil sa byt taky, aby som sedel do davu. Ale odkial prichadza ta nevrazivost? Preco by som vobec
niekomu, kto sa milo nieco opyta realne ani neodpovedal, ani mu neponukol odovodnenie preco a vybavil ho len presne
s tym, na co sa pytal. Preco by som skratoval pokus o konverzaciu, o vymenu informacii? Vseobecny argument je, to su
zeny. Ale ja nesuhlasim, zeny su predsa rovne muzom a maju rovnaku potrebu vymeny informacie ako muzi, a ak je
nejaky problem, tak to vedia a mozu opisat. Mozu mat zaujem, aby to ten clovek pochopil.</br>
Tento zazitok z mladosti, chladne nekompromisne nevysvetlene Nie, je dovod preco som sa bal reality, preco
som to nevedel zlomit, preco som nevedel s niekym, v podstate kymkolvek rozpravat nadviazat vymienat informacia a to bez 
akehokolvek zavazneho umyslu, len tak spontanne. A to ma priviedlo na dalsie chodnicky, kedy som zacal vyuzivat
internet na kontaktovanie zien a muzov ci uz za nejakym ucelom alebo len tak, aby som podvedome obisiel tu 
chladnu odpoved Nie. Obzvlast ked mam ako clovek tuzbu naciahnut sa za laskou za kontaktom a dostanem
takuto neprirodzenu natrenovanu odpoved. A to nejde o to samotne slovo, ide o to ako to bolo povedane, ten pocit.
</br>
A otazka, ktoru si mam klast, nie je, ako zmenit takychto ludi, ako im pomoct? Pretoze aj ked pomoc sa moze javit 
ako velmi mile gesto, pomoct si mozeme iba samy. Moja skutocna otazka ma byt, Preco mam impulz vyhladavat takyto
kontakt, preco vravim, ze ma takyto ludia zaujimaju?</br>
Odpoved je, ze som si myslel, ze to ma takto byt, pretoze som to nevedel s nicim porovnat, 
pretoze som sa s nikym nebozkaval, pretoze som sa s nikym srdecne neobjimal, pretoze som takyto kontakt nezazil,
a dovolym si povedat, vacsina ludi v nasej spolocnosti takyto kontakt nezaziva uplne vedome. Nemyslim v partnerskom
vztahu a intimnom vztahu, ale jednoducho s kymkolvek otvorenym a srdecnym. Mimo tych, s ktorymi sa to patri a mali
by sme a podobne hluposti.
